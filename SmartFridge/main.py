def check_forearm_25(pose_landmarks, threshold=0.20):
    if not pose_landmarks or len(pose_landmarks) == 0:
        return False, 0
    r_elbow = pose_landmarks[0][14]
    r_wrist = pose_landmarks[0][16]
    l_elbow = pose_landmarks[0][13]
    l_wrist = pose_landmarks[0][15]
    samples = 100
    r_points_in_frame = 0
    l_points_in_frame = 0
    for i in range(samples):
        t = i / (samples - 1)
        curr_rx = r_elbow.x + t * (r_wrist.x - r_elbow.x)
        curr_ry = r_elbow.y + t * (r_wrist.y - r_elbow.y)
        if 0 <= curr_rx <= 1 and 0 <= curr_ry <= 1:
            r_points_in_frame += 1
        curr_lx = l_elbow.x + t * (l_wrist.x - l_elbow.x)
        curr_ly = l_elbow.y + t * (l_wrist.y - l_elbow.y)
        if 0 <= curr_lx <= 1 and 0 <= curr_ly <= 1:
            l_points_in_frame += 1
    return (max(l_points_in_frame, r_points_in_frame) / samples) >= threshold, max(l_points_in_frame, r_points_in_frame) / samples

