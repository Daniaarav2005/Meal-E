def process_product_json(response):
    data = response.json()

    if data['status'] != 1:
        return False

    product = data['product']

    name = (product.get('product_name') or
            product.get('product_name_en') or
            product.get('abbreviated_product_name') or
            'Unknown')

    brand = (product.get('brand_owner') or
             product.get('brands') or
             'Unknown')
    brand = brand.split(',')[0].strip()

    # Serving size string
    serving_size = product.get('serving_size') or product.get('quantity') or None

    # All nutriments in the JSON are per 100g, so we scale to per serving
    nutriments = product.get('nutriments', {})

    # Determine serving quantity in grams for scaling
    product_quantity = product.get('product_quantity')
    try:
        serving_grams = float(product_quantity) if product_quantity else 100.0
    except (ValueError, TypeError):
        serving_grams = 100.0

    scale = serving_grams / 100.0

    def get_nutriment(key):
        val = nutriments.get(f'{key}_100g') or nutriments.get(key)
        try:
            return round(float(val) * scale, 4) if val is not None else None
        except (ValueError, TypeError):
            return None

    calories_raw = nutriments.get('energy-kcal') or nutriments.get('energy-kcal_100g')
    try:
        calories = round(float(calories_raw) * scale, 4) if calories_raw is not None else None
    except (ValueError, TypeError):
        calories = None

    quantity = serving_grams if product_quantity else None

    extracted = {
        'name': name,
        'brand': brand,
        'quantity': float(quantity) if quantity else None,
        'calories': calories,
        'carbohydrates': get_nutriment('carbohydrates'),
        'protein': get_nutriment('proteins'),
        'fat': get_nutriment('fat'),
        'saturated_fat': get_nutriment('saturated-fat'),
        'trans_fat': get_nutriment('trans-fat'),
        'sugar': get_nutriment('sugars'),
        'added_sugar': get_nutriment('added-sugars'),
        'fiber': get_nutriment('fiber'),
        'sodium': get_nutriment('sodium'),
        'iron': get_nutriment('iron'),
        'calcium': get_nutriment('calcium'),
        'potassium': get_nutriment('potassium'),
        'vitamin_d': get_nutriment('vitamin-d'),
        'serving_size': serving_size,
        'expiry_date': None,  # Not available in OpenFoodFacts API
    }

    return extracted

