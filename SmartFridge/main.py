def detect_object(frame):
    h, w = frame.shape[:2]

    # Define center region
    cx1, cy1 = w // 4, h // 4
    cx2, cy2 = 3 * w // 4, 3 * h // 4
    center = frame[cy1:cy2, cx1:cx2]

    # Background subtraction on center only
    fg_mask = bg_subtractor.apply(center)

    # Clean up noise
    kernel = cv2.getStructuringElement(cv2.MORPH_ELLIPSE, (5, 5))
    fg_mask = cv2.morphologyEx(fg_mask, cv2.MORPH_OPEN, kernel)
    fg_mask = cv2.dilate(fg_mask, kernel, iterations=2)

    # Only count large connected blobs - filters out noise specks
    contours, _ = cv2.findContours(fg_mask, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
    significant_area = 0
    for cnt in contours:
        area = cv2.contourArea(cnt)
        if area > 500:  # ignore tiny specks
            significant_area += area

    center_area = (cx2 - cx1) * (cy2 - cy1)
    motion_ratio = significant_area / center_area
    motion_detected = motion_ratio > MOTION_THRESHOLD

    # Transition trigger
    # if prev_motion[0] and not motion_detected:
    #     print("TRIGGER - object left frame")
    prev_motion[0] = motion_detected

    return motion_detected, fg_mask

