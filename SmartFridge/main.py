    if inFridge:
        for item in inFridge:
            cursor.execute("""
                INSERT INTO pantry 
                (name, brand, quantity, calories, carbohydrates, protein, fat, saturated_fat, 
                trans_fat, sugar, added_sugar, fiber, sodium, iron, calcium, potassium, 
                vitamin_d, serving_size, expiry_date) 
                VALUES (%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s,%s)
            """, (
                item["name"], item["brand"], item["quantity"], item["calories"],
                item["carbohydrates"], item["protein"], item["fat"], item["saturated_fat"],
                item["trans_fat"], item["sugar"], item["added_sugar"], item["fiber"],
                item["sodium"], item["iron"], item["calcium"], item["potassium"],
                item["vitamin_d"], item["serving_size"], datetime.today() + timedelta(days=item["expiry_date"]) if item["expiry_date"] else None
            ))

    # DELETE
    if outFridge:
        for name in outFridge:
            cursor.execute("DELETE FROM pantry WHERE name = %s", (name,))

    conn.commit()

